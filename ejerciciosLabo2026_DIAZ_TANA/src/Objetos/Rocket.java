package Objetos;


public class Rocket {
    private String nombre;
    private int veloMax;
    private int largo;
    private int ancho;
    private int cantP;


 public Rocket(String nombre , int veloMax , int largo, int ancho, int cantP){
     this.nombre = nombre;
     this.veloMax = veloMax;
     this.largo = largo;
     this.ancho = ancho;
     this.cantP = cantP;
     }

    public String getNombre() {
        return nombre;
    }

    public int getVeloMax(){
     return veloMax;
    }

    public int getLargo(){
     return largo;
    }

    public int getAncho(){
     return ancho;
    }

    public int getCantP(){
     return cantP;
    }





































































































}